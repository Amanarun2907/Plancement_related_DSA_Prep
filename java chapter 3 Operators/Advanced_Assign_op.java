public class Advanced_Assign_op {
    public static void main (String[] args){
        int a = 10;
        int b = a; // b = a 
        System.out.println(b); // 10
        a+=10; // a = a+10;
        System.out.println(a); // 20
        a-=10; // a = a-10;
        System.out.println(a); // 10
        a*=10; // a = a*10;
        System.out.println(a); // 100
        a/=10; // a = a/10;
        System.out.println(a); // 10
        a%=3; // a =  a%3
        System.out.println(a); // 1
    }
}
