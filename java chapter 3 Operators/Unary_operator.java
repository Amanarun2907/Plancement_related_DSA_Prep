public class Unary_operator {
    public static void main(String[] args) {
     int a = 10;
     int b = 5;
     System.out.println(a); //10
     System.out.println(b); //5
     System.out.println(a++); //10
     System.out.println(a); //11
     System.out.println(++a); //12
     System.out.println(a); //12
     System.out.println(--a);//11
     System.out.println(a);//11
     System.out.println(a--);//11
     System.out.println(a);//10
     System.out.println((++a)+(++b));//11+6=17
     System.out.println(++a);
     System.out.println();
    //  int d =100;
    //  int c = d++;
    //  int c = ++d;
    //  System.out.println(c);
    }
}
// ++ is increment operator
// -- is decrement operator
//++a is preincrement operator
// a++ is postincrement operator
//a-- is postdecrement operator
//--a is a predecrement operator 
//a++ means print a then increase the value of a by 1 (post increment of a )
//++a means first increase the value of a by 1 then print a (pre increment of a  )





