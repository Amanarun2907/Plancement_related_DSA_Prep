//Datatypes in java
public class datatypes_in_java {
    public static void main(String[] args) {
        byte b=8;
        System.out.println(b); // 8
        char  ch='a'; // single character only 
        System.out.println(ch);//a
        boolean var=false; // true or false ........
        System.out.println(var); //false
        var = true;
        System.out.println(var); // true
        float price=10.5f;
        System.out.println(price); // 10.5
        int number=25;
        System.out.println(number); //25
        //long
        //double
        short n =240;
        System.out.println(n); //240
        boolean var2=true;
        System.out.println(var2);



    }
    
}
